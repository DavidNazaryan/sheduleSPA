# –°–æ—Ö—Ä–∞–Ω–∏—Ç–µ –∫–∞–∫ restart_server.sh –∏ —Å–¥–µ–ª–∞–π—Ç–µ –∏—Å–ø–æ–ª–Ω—è–µ–º—ã–º: chmod +x restart_server.sh

